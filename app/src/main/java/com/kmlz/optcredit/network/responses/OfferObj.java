package com.kmlz.optcredit.network.responses;

public class OfferObj {

    String OFFER_ID;
    String OFFER_TITLE;
    String OFFER_CONTENT;
    String BANK_NAME;

    public String getOFFER_ID() {
        return OFFER_ID;
    }

    public void setOFFER_ID(String OFFER_ID) {
        this.OFFER_ID = OFFER_ID;
    }

    public String getOFFER_TITLE() {
        return OFFER_TITLE;
    }

    public void setOFFER_TITLE(String OFFER_TITLE) {
        this.OFFER_TITLE = OFFER_TITLE;
    }

    public String getOFFER_CONTENT() {
        return OFFER_CONTENT;
    }

    public void setOFFER_CONTENT(String OFFER_CONTENT) {
        this.OFFER_CONTENT = OFFER_CONTENT;
    }

    public String getBANK_NAME() {
        return BANK_NAME;
    }

    public void setBANK_NAME(String BANK_NAME) {
        this.BANK_NAME = BANK_NAME;
    }
}
